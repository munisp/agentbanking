const runHealthCheck = protectedProcedure
  .input(
    z.object({
      id: z.number().optional(),
      data: z.record(z.string(), z.any()).optional(),
    })
  )
  .mutation(async ({ input, ctx }) => {
    // ── Enforce STATUS_TRANSITIONS state machine ──
    if (typeof input === "object" && "status" in input) {
      const newStatus = (input as any).status as string;
      const currentStatus =
        ((input as any).currentStatus as string) || "pending";
      const allowed =
        STATUS_TRANSITIONS[currentStatus as keyof typeof STATUS_TRANSITIONS];
      if (allowed && !allowed.includes(newStatus)) {
        throw new TRPCError({
          code: "BAD_REQUEST",
          message: `Invalid status transition from ${currentStatus} to ${newStatus}`,
        });
      }
    }
    const txAmount =
      typeof input === "object" && "amount" in input
        ? Number((input as any).amount)
        : 0;
    const fees = calculateFee(txAmount, "transfer");
    const commission = calculateCommission(fees.fee, "transfer");
    const tax = calculateTax(fees.fee, "vat");
    try {
      const db = (await getDb())!;
      if (input.id) {
        const [existing] = await db
          .select()
          .from(deviceLocations)
          .where(eq(deviceLocations.id, input.id))
          .limit(100);
        if (!existing)
          throw new TRPCError({
            code: "NOT_FOUND",
            message: "runHealthCheck: record not found",
          });
        return {
          success: true,
          id: input.id,
          message: "runHealthCheck completed",
          timestamp: new Date().toISOString(),
        };
      }
      const [row] = await db
        .insert(deviceLocations)
        .values(input.data || ({} as Record<string, unknown>))
        .returning();
      return { success: true, ...row, message: "runHealthCheck completed" };
    } catch (error) {
      if (error instanceof TRPCError) throw error;
      throw new TRPCError({
        code: "INTERNAL_SERVER_ERROR",
        message:
          error instanceof Error ? error.message : "Internal server error",
      });
    }
  });

