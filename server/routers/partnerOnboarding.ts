      const offset = input?.offset ?? 0;
      const rows = await db
        .select()
        .from(tenants)
        .orderBy(desc(tenants.createdAt))
        .limit(limit)
        .offset(offset);
      const [totalRow] = await db.select({ value: count() }).from(tenants);
      return {
        items: rows,
        total: Number(totalRow.value),
        domain: "partner_onboard",
        procedure: "list",
      };
